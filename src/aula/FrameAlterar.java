    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        Informacoes infos = new Informacoes();
        Banco banco = new Banco();

        String tit = jTextField2.getText();
        double pre = Double.parseDouble(jTextField3.getText());
        int id = Integer.parseInt(jTextField1.getText());
        int qnt = Integer.parseInt(jTextField4.getText());

        infos.setIdJogo(id);
        infos.setTitulo(tit);
        infos.setPreco(pre);
        infos.setQuantidade(qnt);

        try {
            banco.alterar(infos, id);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FrameAlterar.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(FrameAlterar.class.getName()).log(Level.SEVERE, null, ex);
        }

        JOptionPane.showMessageDialog(null, "Alterado com Sucesso");
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");


    }//GEN-LAST:event_jButton2ActionPerformed

